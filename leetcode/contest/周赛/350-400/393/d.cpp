using TI = tuple<int, int, int>;

struct No {
  int i, j, cur;
  bool operator<(const No &r) const {
    if (i != r.i)
      return i < r.i;
    else if (j != r.j)
      return j < r.j;
    else
      return cur < r.cur;
  }
};

class Solution {
public:
  map<ll, int> vis;
  int n, m;
  VI a, b;

  ll pack(int i, int j, int cur) {
    ll ans = 0;
    ans += (ll(i) << 21) + (j << 17) + cur;
    return ans;
  }

  int dfs(int i, int j, int cur) {
    if (j == m) {
      if (i == n)
        return 0;
      else
        return INF;
    }

    if (i == n)
      return INF;
    if (m - j > n - i)
      return INF;

    ll key = pack(i, j, cur);
    if (has(vis, key))
      return vis[key];

    cur = cur & a[i];

    if (cur < b[j])
      return INF;

    int res = dfs(i + 1, j, cur);
    if (cur == b[j]) {
      int tmp = dfs(i + 1, j + 1, (1 << 17) - 1) + a[i];
      ckmin(res, tmp);
    }

    vis[key] = res;

    return res;
  }

  int minimumValueSum(vector<int> &a_, vector<int> &b_) {
    vis.clear();
    a = a_, b = b_;
    n = SZ(a), m = SZ(b);
    int ans = dfs(0, 0, (1 << 17) - 1);
    if (ans >= INF)
      return -1;
    else
      return ans;
  }
};

