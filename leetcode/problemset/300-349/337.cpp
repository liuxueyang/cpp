/**
 * Definition for a binary tree node.
 * struct TreeNode {
 *     int val;
 *     TreeNode *left;
 *     TreeNode *right;
 *     TreeNode() : val(0), left(nullptr), right(nullptr) {}
 *     TreeNode(int x) : val(x), left(nullptr), right(nullptr) {}
 *     TreeNode(int x, TreeNode *left, TreeNode *right) : val(x), left(left),
 * right(right) {}
 * };
 */
class Solution {
 public:
  using PPI = pair<TNP, int>;

  map<PPI, int> m;

  int dfs(TNP root, int ok) {
    if (!root) return 0;

    if (has(m, PPI(root, ok))) return m[PPI(root, ok)];

    auto &ans = m[{root, ok}];
    auto [val, l, r] = *root;
    int sum{};

    if (ok) {
      sum += val;
      sum += dfs(l, 0);
      sum += dfs(r, 0);
    } else {
      int sum1 = dfs(l, 0), sum2 = dfs(l, 1), sum3 = dfs(r, 0),
          sum4 = dfs(r, 1);
      sum += max(sum1, sum2) + max(sum3, sum4);
    }

    return ans = sum;
  }

  int rob(TreeNode *root) {
    int ans1 = dfs(root, 0), ans2 = dfs(root, 1);
    return max(ans1, ans2);
  }
};

