/**
 * Definition for a binary tree node.
 * struct TreeNode {
 *     int val;
 *     TreeNode *left;
 *     TreeNode *right;
 *     TreeNode() : val(0), left(nullptr), right(nullptr) {}
 *     TreeNode(int x) : val(x), left(nullptr), right(nullptr) {}
 *     TreeNode(int x, TreeNode *left, TreeNode *right) : val(x), left(left),
 * right(right) {}
 * };
 */
class Solution {
public:
  static const int N = 100100;
  char s[N];
  string ans;
  bool ok{};
  int dist{};
  string path{};

  void dfs2(TNP root, int ta, int idx) {
    if (!root || ok)
      return;

    auto [val, l, r] = *root;
    if (val == ta) {
      ok = true;
      dist = idx - 1;
      path = string(s, s + idx - 1);
      return;
    }

    s[idx - 1] = 'L';
    dfs2(l, ta, idx + 1);
    s[idx - 1] = 'R';
    dfs2(r, ta, idx + 1);
  }

  int get_dist(TNP root, int ta) {
    ok = false;
    dist = 0;
    path = {};
    dfs2(root, ta, 1);

    return dist;
  }

  TNP dfs(TNP root, int st, int en) {
    if (!root || root->val == st || root->val == en)
      return root;

    auto [val, l, r] = *root;
    auto lcal = dfs(l, st, en), lcar = dfs(r, st, en);
    if (lcal && lcar) {
      return root;
    }
    return lcal ? lcal : lcar;
  }

  string getDirections(TreeNode *root, int startValue, int destValue) {
    auto lca = dfs(root, startValue, destValue);
    int val = lca->val;

    if (val == startValue) {
      get_dist(lca, destValue);
      return path;
    } else if (val == destValue) {
      int d = get_dist(lca, startValue);
      return string(d, 'U');
    } else {
      int d = get_dist(lca, startValue);
      ans += string(d, 'U');
      get_dist(lca, destValue);
      ans += path;
      return ans;
    }
  }
};

