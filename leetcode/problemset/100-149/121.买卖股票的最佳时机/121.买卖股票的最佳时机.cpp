class Solution {
 public:
  int maxProfit(vector<int> &a) {
    int ans = 0, mie = INF;
    for (auto x : a) {
      int tmp = x - mie;
      ans = max(ans, tmp);
      mie = min(mie, x);
    }
    return ans;
  }
};

