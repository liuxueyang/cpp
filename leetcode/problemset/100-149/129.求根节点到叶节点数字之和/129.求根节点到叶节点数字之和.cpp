class Solution {
 public:
  int ans;

  void dfs(TreeNode *root, int cur) {
    if (!root) {
      return;
    }
    auto [val, l, r] = *root;
    cur = cur * 10 + val;
    if (l) dfs(l, cur);
    if (r) dfs(r, cur);
    if (!l && !r) ans += cur;
  }

  int sumNumbers(TreeNode *root) {
    ans = 0;
    dfs(root, 0);
    return ans;
  }
};

