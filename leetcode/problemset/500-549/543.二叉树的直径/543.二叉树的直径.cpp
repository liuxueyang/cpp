class Solution {
 public:
  int ans;

  int dfs(TreeNode *root) {
    if (!root) return 0;

    auto [_, l, r] = *root;
    int dl = dfs(l), dr = dfs(r);
    int h = 1 + max(dl, dr);
    ckmax(ans, dl + dr + 1);
    return h;
  }

  int diameterOfBinaryTree(TreeNode *root) {
    ans = 0;
    dfs(root);
    return ans - 1;
  }
};

