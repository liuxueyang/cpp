class Solution {
 public:
  vector<int> ans;
  set<int> vis;

  void dfs(TreeNode *root, int dep) {
    if (!root) return;

    auto [val, l, r] = *root;
    if (vis.find(dep) == vis.end()) {
      vis.insert(dep);
      ans.push_back(val);
    }
    if (r) dfs(r, dep + 1);
    if (l) dfs(l, dep + 1);
  }

  vector<int> rightSideView(TreeNode *root) {
    ans = {};
    vis = {};
    dfs(root, 1);
    return ans;
  }
};

