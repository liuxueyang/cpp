/**
 * Definition for a binary tree node.
 * struct TreeNode {
 *     int val;
 *     TreeNode *left;
 *     TreeNode *right;
 *     TreeNode() : val(0), left(nullptr), right(nullptr) {}
 *     TreeNode(int x) : val(x), left(nullptr), right(nullptr) {}
 *     TreeNode(int x, TreeNode *left, TreeNode *right) : val(x), left(left),
 * right(right) {}
 * };
 */
class Solution {
public:
  VI a, b;
  int n;

  TNP dfs(int l1, int r1, int l2, int r2) {
    if (l1 > r1 || l2 > r2)
      return nullptr;
    if (l1 == r1 || l2 == r2)
      return new TN(a[l1]);

    int val = a[l1 + 1], idx = r2;

    For(i, l2, r2) {
      if (b[i] == val) {
        idx = i;
        break;
      }
    }

    int len = idx - l2 + 1;
    return new TN(a[l1], dfs(l1 + 1, l1 + len, l2, idx),
                  dfs(l1 + len + 1, r1, idx + 1, r2 - 1));
  }

  TreeNode *constructFromPrePost(vector<int> &preorder,
                                 vector<int> &postorder) {
    a = preorder, b = postorder, n = SZ(a);
    return dfs(0, n - 1, 0, n - 1);
  }
};
