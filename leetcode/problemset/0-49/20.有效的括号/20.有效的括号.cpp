class Solution {
 public:
  bool isValid(string s) {
    stack<char> stk;
    for (auto c : s) {
      if (c == '(' || c == '[' || c == '{')
        stk.push(c);
      else {
        if (stk.empty()) return false;
        char c1 = stk.top();
        stk.pop();

        if ((c == ')' && c1 == '(') || (c == ']' && c1 == '[') ||
            (c == '}' && c1 == '{')) {
          continue;
        } else
          return false;
      }
    }
    return stk.empty();
  }
};

