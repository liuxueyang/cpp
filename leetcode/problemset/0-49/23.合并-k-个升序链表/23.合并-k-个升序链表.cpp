class Solution {
 public:
  struct Node {
    ListNode *node;
    bool operator>(const Node &rh) const { return node->val > rh.node->val; }
  };

  ListNode *mergeKLists(vector<ListNode *> &lists) {
    ListNode *du = new ListNode();
    ListNode *cur = du;
    priority_queue<Node, vector<Node>, greater<Node>> q;

    for (auto x : lists) {
      if (x) q.push({x});
    }

    while (!q.empty()) {
      auto x = q.top();
      q.pop();
      cur->next = x.node;
      cur = cur->next;
      if (x.node->next) {
        q.push({x.node->next});
      }
    }
    cur->next = nullptr;

    ListNode *res = du->next;
    delete du;
    return res;
  }
};

