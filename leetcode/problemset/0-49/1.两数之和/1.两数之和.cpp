class Solution {
 public:
  vector<int> twoSum(vector<int> &nums, int target) {
    vector<int> ans(2);
    int n = int(nums.size());
    map<int, int> mp;

    for (int i = 0; i < n; i++) {
      int y = target - nums[i];
      if (mp.count(y)) {
        ans[0] = mp[y];
        ans[1] = i;
        break;
      }
      mp[nums[i]] = i;
    }
    return ans;
  }
};

