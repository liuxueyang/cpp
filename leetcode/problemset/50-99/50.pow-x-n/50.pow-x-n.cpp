class Solution {
 public:
  double qpow(double x, ll n) {
    double res = 1;
    while (n) {
      if (n & 1) res = res * x;
      x = x * x;
      n >>= 1;
    }
    return res;
  }

  double myPow(double x, int n) {
    bool flag = false;
    ll m = n;
    if (m < 0) {
      flag = true;
      m = -m;
    }

    double ans = qpow(x, m);
    if (flag) ans = 1.0 * 1 / ans;
    return ans;
  }
};

