class Solution {
 public:
  vector<vector<int>> merge(vector<vector<int>> &a) {
    vector<pair<int, int>> b;
    for (auto &vec : a) {
      b.push_back({vec[0], vec[1]});
    }

    sort(b.begin(), b.end());
    vector<vector<int>> ans;
    int n = int(b.size());
    auto [cur_l, cur_r] = b[0];

    for (int i = 1; i < n; i++) {
      auto [l, r] = b[i];
      if (l > cur_r) {
        ans.push_back({cur_l, cur_r});
        cur_l = l;
        cur_r = r;
      } else {
        cur_r = max(cur_r, r);
      }
    }
    ans.push_back({cur_l, cur_r});
    return ans;
  }
};

